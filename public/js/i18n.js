// State manager for language selection
let currentLang = localStorage.getItem('swarashtra_lang') || 'en';

function getTranslation(key) {
  return i18nDictionary[currentLang][key] || key;
}

function getCurrentLanguage() {
  return currentLang;
}

function setLanguage(lang) {
  if (lang === 'en' || lang === 'hi') {
    currentLang = lang;
    localStorage.setItem('swarashtra_lang', lang);
    document.documentElement.lang = lang;
    translateUI();
    
    // Dispatch custom event so dynamic scripts know language updated
    window.dispatchEvent(new CustomEvent('langChanged', { detail: lang }));
  }
}

// Scans document and updates elements containing data-i18n attribute
function translateUI() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = getTranslation(key);
    
    // Handle input placeholders
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
      el.setAttribute('placeholder', translation);
    } else {
      el.textContent = translation;
    }
  });
}

// Run on page load
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = currentLang;
  translateUI();
  
  // Set language selector dropdown value if exists
  const selector = document.getElementById('languageSwitcher');
  if (selector) {
    selector.value = currentLang;
    selector.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
});
